// types de données
// #################
//number
let num = 23;

// débogage
console.log("num :", typeof num, num); // typeof "num", type variable second "num" valeur (num : number 23)

// string
let str = "hello";

// débogage
console.log("str :", typeof str, str); // str : string hello

// boolean
let bool = true; //ou false

// débogage
console.log("bool :", typeof bool, bool); // bool : boolean true
bool = !bool; // contraire → false
console.log("bool :", typeof bool, bool); // bool : boolean false

// null
let nothing = null;

// débogage
console.log("nothing :", typeof nothing, nothing); // nothing : object null

//array
let tab = []; // let tab = neaw Array()
//débogage
console.log("tab :", typeof tab, tab); // tab : object []
